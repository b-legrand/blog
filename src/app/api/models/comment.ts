/* tslint:disable */
export interface Comment {

  /**
   * The comment body.
   */
  body?: string;

  /**
   * The email of the user who posted the comment.
   */
  email?: string;

  /**
   * The comment id.
   */
  id?: string;

  /**
   * The name of the comment.
   */
  name?: string;

  /**
   * The comment postId.
   */
  postId?: string;
}
